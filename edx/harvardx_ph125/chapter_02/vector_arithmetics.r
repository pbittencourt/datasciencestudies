# Define a general vector
v <- c(1, 1, 2, 3, 5, 8, 13, 21)

# In R, operations on vector occurs *element-wise*:
v3 <- v * 3
v3

# Two vectors of the same length can be operated
a <- c(1, 2, 3, 4)
b <- c(4, 8, 12, 16)
soma <- a + b
soma
diferenca <- a - b
diferenca
multiplication <- a * b
multiplication
division <- a / b
division
